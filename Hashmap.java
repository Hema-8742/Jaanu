import java.util.HashMap;
class Hashmap{ 
  public static void main(String[] args) {
     HashMap<Integer, String> hmap = new HashMap<Integer, String>();
     hmap.put(1, "HEMA");
     hmap.put(2, "RED");
     hmap.put(3, "COLOR");
     hmap.put(4, "DONE");
     System.out.println("HashMap contains: "+hmap);
  }
}